package com.belajar;

public class BelajarForLoop {
    public static void main(String[] args) {
        System.out.println("Perulangan Pertama");
        for(int i = 1; i <= 10; i++){
            System.out.println("Perulangan ke - " + i);
        }
        System.out.println("Perulangan Kedua");
        for(int i = 10; i >= 1; i--){
            System.out.println("Perulangan ke - " + i);
        }
    }
}
